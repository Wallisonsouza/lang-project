#pragma once

#include <stdexcept>
#include <string>

class StreamException : public std::runtime_error {
public:
  explicit StreamException(const std::string &msg) : std::runtime_error(msg) {}
};

class StreamEndException : public StreamException {
public:
  StreamEndException() : StreamException("End of stream reached") {}
};

class StreamBeginException : public StreamException {
public:
  StreamBeginException() : StreamException("Already at beginning of stream") {}
};
